# This program calculates the product of three integers entered by the user

# Prompt the user to enter the first integer
num1 = int(input("Enter the first integer: "))

# Prompt the user to enter the second integer
num2 = int(input("Enter the second integer: "))

# Prompt the user to enter the third integer
num3 = int(input("Enter the third integer: "))

# Calculate the product of the three integers
product = num1 * num2 * num3

# Print the result to the console
print("The product of", num1, ",", num2, ", and", num3, "is", product)
