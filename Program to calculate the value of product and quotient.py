# This program calculates the value of product and quotient
num1 = 10
num2 = 5

# Calculate the product
product = num1 * num2

# Calculate the quotient
quotient = num1 / num2

# Print the results
print(f"The product of {num1} and {num2} is {product}")
print(f"The quotient of {num1} and {num2} is {quotient}")
