import random

# generate a random integer between 1 and 10
rand_int = random.randint(1, 10)

# shift the range by 5
shifted_int = rand_int + 5

# scale the range by 3
scaled_int = shifted_int * 3

# print the results
print("Random integer: ", rand_int)
print("Shifted integer: ", shifted_int)
print("Scaled integer: ", scaled_int)
